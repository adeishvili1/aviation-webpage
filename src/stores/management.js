export const useManagementStore = defineStore('management', () => {
  const saved = localStorage.getItem('tam_management')
  const members = ref(saved ? JSON.parse(saved) : DEFAULT_MEMBERS)

  // Auto-persist every change
  watch(members, (val) => {
    localStorage.setItem('tam_management', JSON.stringify(val))
  }, { deep: true })

  function updateMember(updated) {
    const i = members.value.findIndex(m => m.id === updated.id)
    if (i !== -1) members.value[i] = updated
  }

  function reorderMembers(newOrder) {
    members.value = newOrder
  }

  function resetToDefault() {
    members.value = DEFAULT_MEMBERS
    localStorage.removeItem('tam_management')
  }

  return { members, updateMember, reorderMembers, resetToDefault }
})
